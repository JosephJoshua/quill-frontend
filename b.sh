# Install shadcn/ui dependencies and other essential libraries
npm install zustand react-router-dom lucide-react class-variance-authority clsx tailwind-merge tailwindcss-animate

# Create the shadcn/ui config file
cat <<'EOF' >components.json
{
  "$schema": "https://ui.shadcn.com/schema.json",
  "style": "default",
  "rsc": false,
  "tsx": true,
  "tailwind": {
    "config": "tailwind.config.js",
    "css": "src/index.css",
    "baseColor": "slate",
    "cssVariables": true
  },
  "aliases": {
    "components": "@/components",
    "utils": "@/lib/utils"
  }
}
EOF

# Add path alias to vite.config.ts
# We use a temporary file to prepend the import and then modify the config
cat <<'EOF' >vite.config.ts.tmp
import path from "path"
import react from '@vitejs/plugin-react'
import { defineConfig } from 'vite'

export default defineConfig({
  plugins: [react()],
  resolve: {
    alias: {
      "@": path.resolve(__dirname, "./src"),
    },
  },
})
EOF
mv vite.config.ts.tmp vite.config.ts

# Create the lib directory and the utils.ts file for shadcn/ui
mkdir -p src/lib
cat <<'EOF' >src/lib/utils.ts
import { type ClassValue, clsx } from "clsx"
import { twMerge } from "tailwind-merge"

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs))
}
EOF

